const mongoose = require("mongoose");
const Schema = mongoose.Schema;
// Create Schema
const AccountSchema = new Schema({
  firstName: {
    type: String,
    trim: true,
    required: true
  },
  lastName: {
    type: String,
    trim: true,
    required: true
  },
  email: {
    type: String,
    unique: true,
    trim: true,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const Account = mongoose.model('Account', AccountSchema);

module.exports = Account;